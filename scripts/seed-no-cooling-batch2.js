(async () => {
  const client = new MongoClient(MONGODB_URI);
  try {
    await client.connect();
    const db  = client.db('clientsvia');
    const col = db.collection('companyKnowledgeContainers');

    // Find existing container
    const existing = await col.findOne({
      companyId: COMPANY_ID,
      kcId:      KC_ID,
    });

    if (!existing) {
      console.error(`Container ${KC_ID} not found for company ${COMPANY_ID}`);
      process.exit(1);
    }

    const currentCount = (existing.sections || []).length;
    console.log(`Found "${existing.title}" with ${currentCount} existing sections`);

    // Build sections with order starting after existing ones
    const sectionsToAdd = NEW_SECTIONS.map((s, i) => ({
      label:            s.label,
      content:          s.content,
      callerPhrases:    s.callerPhrases,
      negativeKeywords: s.negativeKeywords,
      contentKeywords:  [],          // auto-generated later or via admin UI
      useFixedResponse: true,
      isActive:         true,
      isPromotion:      false,
      order:            currentCount + i,
    }));

    // Append sections using $push + $each
    const result = await col.updateOne(
      { _id: existing._id },
      {
        $push: { sections: { $each: sectionsToAdd } },
        $set:  { updatedAt: new Date() },
      }
    );

    if (result.modifiedCount === 1) {
      console.log(`\n✅ SUCCESS: Added ${sectionsToAdd.length} new sections to "${existing.title}"`);
      console.log(`   Total sections now: ${currentCount + sectionsToAdd.length}`);
      console.log(`   Total phrases added: ${sectionsToAdd.reduce((sum, s) => sum + s.callerPhrases.length, 0)}`);
    } else {
      console.error('❌ Update did not modify the document');
    }
  } catch (err) {
    console.error('❌ Error:', err.message);
    process.exit(1);
  } finally {
    await client.close();
  }
})();
