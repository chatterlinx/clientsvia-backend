// ── Push to DB ───────────────────────────────────────────────────────────────

(async () => {
  const client = new MongoClient(MONGODB_URI);
  try {
    await client.connect();
    const db = client.db('clientsvia');
    const col = db.collection('companyKnowledgeContainers');

    // Verify container exists
    const container = await col.findOne({
      companyId: new ObjectId(COMPANY_ID),
      kcId: KC_ID,
    });
    if (!container) {
      console.error(`Container ${KC_ID} not found for company ${COMPANY_ID}`);
      process.exit(1);
    }

    const now = new Date();
    const sections = NEW_SECTIONS.map((s) => ({
      label:            s.label,
      content:          s.content,
      callerPhrases:    s.callerPhrases,
      negativeKeywords: s.negativeKeywords,
      useFixedResponse: true,
      isActive:         true,
      isPromotion:      false,
      audioUrl:         '',
      createdAt:        now,
      updatedAt:        now,
    }));

    const result = await col.updateOne(
      { companyId: new ObjectId(COMPANY_ID), kcId: KC_ID },
      { $push: { sections: { $each: sections } } },
    );

    console.log(`✅ Pushed ${sections.length} sections to ${KC_ID}`);
    console.log(`   matchedCount: ${result.matchedCount}, modifiedCount: ${result.modifiedCount}`);
    console.log(`   Container "${container.title}" now has ${(container.sections?.length || 0) + sections.length} sections`);
  } catch (err) {
    console.error('Fatal:', err);
    process.exit(1);
  } finally {
    await client.close();
  }
})();
